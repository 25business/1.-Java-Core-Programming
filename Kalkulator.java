public class Kalkulator {
	public static void main(String[] args) {
		
		double a = 5.5;
		double b = 7.7;
		
		System.out.println("Sabiranje brojeva: 5.5 + 7.7 = " + (a + b));
		System.out.println("Oduzimanje brojeva: 5.5 - 7.7 = " + (a - b));
		System.out.println("Mnozenje brojeva: 5.5 * 7.7 = " + (a * b));
		System.out.println("Deljenje brojeva: 5.5 / 7.7 = " + (a / b));
	}
}